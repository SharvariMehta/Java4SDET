package ex_11_Object_Oriented_Programs;

public class User_Defined_Function {
    public static void main(String[] args) {
        function1();    // Calling a function
    }
    //Defining a Function
    static void function1()
    {
        System.out.println("First User Defined Function Program");
    }
}
