package ex_01_Java_Basics;

public class Main_Without_Space {
    public static void main(String[]args) {
        System.out.println("No space between [] and args (String[]args)—that’s valid Java syntax." +
                "It's equivalent to String[] args with a space." +
                "\n" +
                "JVM recognizes it just fine." +
                "\n" +
                "Most code standards add a space (String[] args) for readability, but it's not a requirement.");
    }
}
