package ex_01_Java_Basics;

public class MultipleInputViaConsole {
    public static void main(String[] args) {
        String a1 = args[0];
        String a2 = args[1];
        String a3 = args[3];
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
    }
}
/*
OUTPUT:
10
20
40
 */