package ex_03_Typecasting_and_Operators;

public class DecrementOperator {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a--);
        System.out.println(a);

        int b = 12;
        System.out.println(--b);
        System.out.println(b);
    }
}
/*
OUTPUT:
10
9
11
11
 */