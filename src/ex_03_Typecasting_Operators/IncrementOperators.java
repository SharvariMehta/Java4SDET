package ex_03_Typecasting_Operators;

public class IncrementOperators {
    public static void main(String[] args) {

        //Post Increment Operator
        int a = 10;
        System.out.println(a++);
        System.out.println(a);

        //Pre Increment Operator
        int b = 15;
        System.out.println(++b);
        System.out.println(b);
    }
}
