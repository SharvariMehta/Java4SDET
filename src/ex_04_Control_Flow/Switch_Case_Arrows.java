package ex_04_Control_Flow;

public class Switch_Case_Arrows {
    public static void main(String[] args) {
        int n = 001;
        switch(n)
        {
            case 001 -> System.out.println("001");
            case 002 -> System.out.println("001");
            case 003,004 -> System.out.println("001");
        }
    }
}
/*
OUTPUT:
001
 */